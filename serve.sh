#!/bin/bash
# Простой и удобный скрипт запуска локального HTTP-сервера
# для папки с твоим сайтом.
#
# Как использовать:
#   1. chmod +x serve.sh   (сделать исполняемым)
#   2. ./serve.sh
#
# Сайт будет доступен по адресу:
#   http://localhost:8000/

cd "$(dirname "$0")" || exit 1

PORT=8000

echo "=========================================="
echo "  Локальный сервер для сайта"
echo "=========================================="
echo ""
echo "Папка: $(pwd)"
echo "Адрес: http://localhost:${PORT}/"
echo ""
echo "Нажми Ctrl+C чтобы остановить."
echo ""

# Запускаем сервер
python3 -m http.server "$PORT" --bind 127.0.0.1
